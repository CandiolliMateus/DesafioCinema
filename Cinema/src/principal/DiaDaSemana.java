package principal;

public class DiaDaSemana {
	
	private final int outros = 1;
	private final int segunda = 2;
	private final int terca = 3;
	private final int quarta = 4;
	private final int quinta = 5;
	private final int sexta = 6;
	
	public DiaDaSemana() {
		
	}

	public int getOutros() {
		return outros;
	}

	public int getSegunda() {
		return segunda;
	}

	public int getTerca() {
		return terca;
	}

	public int getQuarta() {
		return quarta;
	}

	public int getQuinta() {
		return quinta;
	}

	public int getSexta() {
		return sexta;
	}
}
